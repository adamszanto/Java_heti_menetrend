public class Futo{
    private String name;
    private int tav;

    public Futo() {
    }

    public Futo(String name, int tav) {
        this.name = name;
        this.tav = tav;
    }

    public int getTav() {
        return tav;
    }

    public String getName() {
        return name;
    }

}
